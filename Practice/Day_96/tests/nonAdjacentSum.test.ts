import { describe, expect, it } from 'vitest'
import { nonAdjacentSum } from '../src/nonAdjacentSum'

describe('nonAdjacentSum', () => {
  it.each([
    [[2, 4, 5, 12, 7], 16], // 4 + 12 = 16
    [[7, 5, 5, 12], 19], // 7 + 12 = 19
    [[7, 5, 5, 12, 17, 29], 48], // 7 + 12 + 29 = 48
    [
      [
        72, 62, 10, 6, 20, 19, 42, 46, 24, 78, 30, 41, 75, 38, 23, 28, 66, 55,
        12, 17, 9, 12, 3, 1, 19, 30, 50, 20,
      ],
      488,
    ],
    [
      [
        72, 62, 10, 6, 20, 19, 42, 46, 24, 78, 30, 41, 75, 38, 23, 28, 66, 55,
        12, 17, 83, 80, 56, 68, 6, 22, 56, 96, 77, 98, 61, 20, 0, 76, 53, 74, 8,
        22, 92, 37, 30, 41, 75, 38, 23, 28, 66, 55, 12, 17, 72, 62, 10, 6, 20,
        19, 42, 46, 24, 78, 42,
      ],
      1465,
    ],
  ])('should return %i for input %j', (input, expected) => {
    expect(nonAdjacentSum(input)).toBe(expected)
  })
})
